
package historiasclinicas;


public class historia {
    private int nhistoria;
    private String fecha;
    private int id_paciente;
    private int id_medico;
    private String motivo;
    private String enfactual;
    private String diagnostico;
    private String tratamiento;

    /**
     * @return the nhistoria
     */
    public int getNhistoria() {
        return nhistoria;
    }

    /**
     * @param nhistoria the nhistoria to set
     */
    public void setNhistoria(int nhistoria) {
        this.nhistoria = nhistoria;
    }

    /**
     * @return the fecha
     */
    public String getFecha() {
        return fecha;
    }

    /**
     * @param fecha the fecha to set
     */
    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    /**
     * @return the id_paciente
     */
    public int getId_paciente() {
        return id_paciente;
    }

    /**
     * @param id_paciente the id_paciente to set
     */
    public void setId_paciente(int id_paciente) {
        this.id_paciente = id_paciente;
    }

    /**
     * @return the id_medico
     */
    public int getId_medico() {
        return id_medico;
    }

    /**
     * @param id_medico the id_medico to set
     */
    public void setId_medico(int id_medico) {
        this.id_medico = id_medico;
    }

    /**
     * @return the motivo
     */
    public String getMotivo() {
        return motivo;
    }

    /**
     * @param motivo the motivo to set
     */
    public void setMotivo(String motivo) {
        this.motivo = motivo;
    }

    /**
     * @return the enfactual
     */
    public String getEnfactual() {
        return enfactual;
    }

    /**
     * @param enfactual the enfactual to set
     */
    public void setEnfactual(String enfactual) {
        this.enfactual = enfactual;
    }

    /**
     * @return the diagnostico
     */
    public String getDiagnostico() {
        return diagnostico;
    }

    /**
     * @param diagnostico the diagnostico to set
     */
    public void setDiagnostico(String diagnostico) {
        this.diagnostico = diagnostico;
    }

    /**
     * @return the tratamiento
     */
    public String getTratamiento() {
        return tratamiento;
    }

    /**
     * @param tratamiento the tratamiento to set
     */
    public void setTratamiento(String tratamiento) {
        this.tratamiento = tratamiento;
    }
            
}
